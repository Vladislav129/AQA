package Lesson_7;

public class Comparison {
    public boolean comp1(int a, int b) {
        return a != b;
    }
    public boolean comp2(int a, int b) {
        return a > b;
    }
    public boolean comp3(int a, int b) {
        return a < b;
    }
    public boolean comp4(int a, int b) {
        return a == b;
    }
}

